class TenantFeatureFlag(BaseModel):
    """
    Granular per-tenant feature toggle.

    Allows enabling or disabling specific features independently of the
    subscription plan. One row per feature per tenant.

    Why per-row instead of a single JSONB column on tenants?
      - Each flag has its own enable/disable timestamp and actor
      - Flags can have individual expiry dates (temporary early access)
      - Querying "which tenants have feature X" is efficient
      - Audit trail per flag

    Examples of feature_key values:
      'AI_LESSON_SUGGESTIONS'     → GPT-powered lesson plan suggestions
      'PARENT_WHATSAPP_ALERTS'    → WhatsApp notification channel
      'ADVANCED_ANALYTICS'        → BI dashboards beyond standard reports
      'BULK_STUDENT_IMPORT_V2'    → New importer in beta
      'MULTI_BRANCH'              → Multiple campuses under one tenant
      'CUSTOM_REPORT_BUILDER'     → Drag-drop report designer
      'API_WEBHOOKS'              → Webhook delivery for events
      'SSO_GOOGLE'                → Google Workspace SSO
      'SSO_MICROSOFT'             → Microsoft/Azure SSO
      'DATA_EXPORT_CSV'           → Export data to CSV
      'WHITE_LABEL_DOMAIN'        → Custom domain mapping
      'EXAM_OMR_INTEGRATION'      → OMR sheet scanning integration
    """

    __tablename__ = "tenant_feature_flags"

    # ── References ────────────────────────────────────────────────────────────
    tenant_id = Column(
        UUID(as_uuid=True),
        nullable=False,
        index=True,
        comment="FK → tenants.id. The tenant this flag applies to.",
    )

    # ── Flag Identity ─────────────────────────────────────────────────────────
    feature_key = Column(
        String(100),
        nullable=False,
        index=True,
        comment=(
            "Machine-readable feature key. UPPER_SNAKE_CASE. "
            "Must match the key checked in application code. "
            "E.g. 'AI_LESSON_SUGGESTIONS'"
        ),
    )

    # ── State ─────────────────────────────────────────────────────────────────
    is_enabled = Column(
        String(5),
        nullable=False,
        default="false",
        index=True,
        comment="Whether this feature is currently enabled for this tenant",
    )

    # ── Validity ──────────────────────────────────────────────────────────────
    enabled_from = Column(
        Date,
        nullable=True,
        comment="Date this feature was enabled (null = enabled immediately on creation)",
    )
    enabled_until = Column(
        Date,
        nullable=True,
        comment=(
            "Date this feature auto-disables. "
            "NULL = no expiry (permanent). "
            "Used for time-limited early access programs."
        ),
    )

    # ── Configuration ─────────────────────────────────────────────────────────
    config = Column(
        JSONB,
        nullable=False,
        default=dict,
        comment=(
            "Feature-specific configuration. "
            "E.g. for 'PARENT_WHATSAPP_ALERTS': "
            "{provider: 'gupshup', api_key_secret_ref: 'secret/tenant/gupshup'}"
        ),
    )

    # ── Audit ─────────────────────────────────────────────────────────────────
    enabled_by_user_id = Column(
        UUID(as_uuid=True),
        nullable=True,
        comment="Internal SmartSync user_id who enabled this flag",
    )
    reason = Column(
        String(500),
        nullable=True,
        comment="Why this flag was enabled/disabled. For internal audit.",
    )

    # ── Relationships ─────────────────────────────────────────────────────────
    tenant = relationship(
        "Tenant",
        back_populates="feature_flags",
        foreign_keys=[tenant_id],
    )

    def __repr__(self) -> str:
        return (
            f"<TenantFeatureFlag tenant_id={self.tenant_id} "
            f"feature={self.feature_key!r} enabled={self.is_enabled}>"
        )
        